#[test]
fn test_reversible() {
    let crypto = ConnEncryption::default();
    crypto.set_cipher_key(*b"1234567890123456");

    let mut packet = vec![0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09];
    block_on(crypto.encrypt_inplace(&mut packet));
    block_on(crypto.decrypt_inplace(&mut packet));
    assert_eq!(packet, vec![0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09]);

    let mut packet = vec![0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09];
    block_on(crypto.encrypt_inplace(&mut packet));
    block_on(crypto.decrypt_inplace(&mut packet));
    block_on(crypto.encrypt_inplace(&mut packet));
    block_on(crypto.decrypt_inplace(&mut packet));
    assert_eq!(packet, vec![0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09]);

    let mut packet = vec![0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09];
    block_on(crypto.encrypt_inplace(&mut packet));
    block_on(crypto.encrypt_inplace(&mut packet));
    block_on(crypto.decrypt_inplace(&mut packet));
    block_on(crypto.decrypt_inplace(&mut packet));
    assert_ne!(packet, vec![0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09]);
}
