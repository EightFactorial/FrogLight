use froglight_packet::network::ConnEncryption;
use futures_lite::future::block_on;

#[test]
fn test_encryption() {
    let crypto = ConnEncryption::default();
    crypto.set_cipher_key(*b"1234567890123456");

    let mut packet = vec![0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09];
    block_on(crypto.encrypt_inplace(&mut packet));
    assert_eq!(packet, vec![0x75, 0x97, 0xb7, 0x2d, 0xe5, 0xe8, 0x2b, 0xb5, 0x79, 0x10]);

    let mut packet = vec![0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10, 0x11, 0x12, 0x13];
    block_on(crypto.encrypt_inplace(&mut packet));
    assert_eq!(packet, vec![0xb9, 0xdf, 0x81, 0x99, 0xad, 0x8c, 0x85, 0xef, 0x3b, 0xa8]);
}

